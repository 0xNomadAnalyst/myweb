import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, "..");

const sourceDir = path.join(root, "public", "dashboard", "carousel");
const outDir = path.join(root, "lib", "generated");
const outFile = path.join(outDir, "dashboard-screenshots.generated.ts");
const watchMode = process.argv.includes("--watch");

function toAltText(fileName) {
  const base = fileName.replace(/\.webp$/i, "");
  const words = base
    .replace(/[-_]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .toLowerCase();
  return `${words} dashboard view`;
}

function escapeForTs(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

fs.mkdirSync(sourceDir, { recursive: true });
fs.mkdirSync(outDir, { recursive: true });

function generate() {
  const files = fs
    .readdirSync(sourceDir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && /\.webp$/i.test(entry.name))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));

  const rows = files.map((name) => {
    const src = `/dashboard/carousel/${name}`;
    const alt = toAltText(name);
    return `  { src: "${escapeForTs(src)}", alt: "${escapeForTs(alt)}" },`;
  });

  const output = `// AUTO-GENERATED by scripts/generate-dashboard-screenshots.mjs
// Do not edit manually.

export type DashboardScreenshot = {
  src: string;
  alt: string;
};

export const dashboardScreenshots: DashboardScreenshot[] = [
${rows.join("\n")}
];
`;

  fs.writeFileSync(outFile, output, "utf8");
  console.log(
    `[dashboard-screenshots] wrote ${files.length} entries to ${path.relative(root, outFile)}`,
  );
}

generate();

if (watchMode) {
  let debounce = null;
  console.log(`[dashboard-screenshots] watching ${path.relative(root, sourceDir)} for .webp changes...`);
  fs.watch(sourceDir, () => {
    if (debounce) clearTimeout(debounce);
    debounce = setTimeout(() => {
      generate();
    }, 120);
  });
}
